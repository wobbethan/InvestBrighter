import { createReducer } from "@reduxjs/toolkit";

const initialState = {
  isLoading: true,
};

export const productReducer = createReducer(initialState, {
  productCreateRequest: (state) => {
    state.isLoading = true;
  },
  productCreateSuccess: (state, action) => {
    state.isLoading = false;
    state.product = action.payload;
    state.success = true;
  },
  productCreateFail: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
    state.success = false;
  },

  //all products for shop
  getAllProductsShopRequest: (state) => {
    state.isLoading = true;
  },
  getAllProductsShopSuccess: (state, action) => {
    state.isLoading = false;
    state.products = action.payload;
  },
  getAllProductsShopFail: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
  },

  // delete product of a shop
  deleteProductRequest: (state) => {
    state.isLoading = true;
  },
  deleteProductSuccess: (state, action) => {
    state.isLoading = false;
    state.message = action.payload;
  },
  deleteProductFailed: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
  },

  // get all products
  getAllProductsRequest: (state) => {
    state.isLoading = true;
  },
  getAllProductsSuccess: (state, action) => {
    state.isLoading = false;
    state.allProducts = action.payload;
  },
  getAllProductsFailed: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
  },

  // get all admin
  getAllProductsAdminRequest: (state) => {
    state.isLoading = true;
  },
  getAllProductsAdminSuccess: (state, action) => {
    state.isLoading = false;
    state.allProductsAdmin = action.payload;
  },
  getAllProductsAdminFailed: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
  },

  // get all section
  getAllProductsSectionRequest: (state) => {
    state.isLoading = true;
  },
  getAllProductsSectionSuccess: (state, action) => {
    state.isLoading = false;
    state.allProductsSection = action.payload;
  },
  getAllProductsSectionFailed: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
  },

  // get all event
  getAllProductsEventRequest: (state) => {
    state.isLoading = true;
  },
  getAllProductsEventSuccess: (state, action) => {
    state.isLoading = false;
    state.allProductsEvent = action.payload;
  },
  getAllProductsEventFailed: (state, action) => {
    state.isLoading = false;
    state.error = action.payload;
  },

  clearErrors: (state) => {
    state.error = null;
  },
});
